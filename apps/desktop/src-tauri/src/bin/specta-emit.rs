// specta-emit — standalone TS bindings emitter.
//
// Regenerates `packages/shared-types/src/ipc.ts` from `ipc_spec::builder()`
// without launching the full Tauri runtime. Used by:
//   * Plan 10 release CI (`cargo run --bin specta-emit` then `git diff
//     --exit-code` to ensure the committed bindings stay in sync).
//   * Local devs who want to refresh bindings without booting the app.
//
// Usage:
//   cargo run --bin specta-emit
//   cargo run --bin specta-emit -- --out path/to/ipc.ts

fn main() {
    let mut args = std::env::args().skip(1);
    let out_path = args
        .next()
        .unwrap_or_else(|| storycapture::ipc_spec::TS_BINDINGS_PATH.to_string());
    let builder = storycapture::ipc_spec::builder();
    let header = "// AUTO-GENERATED by tauri-specta — DO NOT EDIT.\n\
                  // Source: apps/desktop/src-tauri/src/ipc_spec.rs (Phase 1 plan 01-03).\n\
                  // Regenerate with: `cargo run --bin specta-emit`.\n";
    builder
        .export(specta_typescript::Typescript::default().header(header), &out_path)
        .expect("specta TS export");
    eprintln!("[specta-emit] wrote {}", out_path);
}
