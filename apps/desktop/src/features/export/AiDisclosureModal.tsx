/**
 * AI Disclosure Modal (Plan 03-20, Task 2 / AI-SPEC G8).
 *
 * EU AI Act Article 50 export-time disclosure.
 * Shown when project contains >= 1 TTS clip on export.
 *
 * Title: "This export contains AI-generated voiceover"
 * Body: EU AI Act Art. 50 reference.
 * Checkbox: "Embed C2PA/XMP AI-generated marker" (default ON).
 * "Learn more" link -> docs/compliance/ai-act.md
 * Buttons: "Export anyway" / "Cancel"
 *
 * Phase 2 export flow invokes this component when project.tts_clips.count > 0.
 * TODO: Integration with Phase 2 export via event bus when Phase 2 export is shipped.
 */

import { useState, useCallback } from "react";
import { Button } from "@/components/ui/button";
import { ExternalLink } from "lucide-react";

export interface AiDisclosureResult {
  proceed: boolean;
  embedC2pa: boolean;
}

export interface AiDisclosureModalProps {
  open: boolean;
  ttsClipCount: number;
  onResult: (result: AiDisclosureResult) => void;
}

export function AiDisclosureModal({
  open,
  ttsClipCount,
  onResult,
}: AiDisclosureModalProps) {
  // C2PA checkbox default ON per G8
  const [embedC2pa, setEmbedC2pa] = useState(true);

  const handleExport = useCallback(() => {
    onResult({ proceed: true, embedC2pa });
  }, [onResult, embedC2pa]);

  const handleCancel = useCallback(() => {
    onResult({ proceed: false, embedC2pa: false });
  }, [onResult]);

  if (!open || ttsClipCount < 1) {
    return null;
  }

  return (
    <div
      role="alertdialog"
      aria-modal="true"
      className="fixed inset-0 z-50 flex items-center justify-center bg-black/50"
    >
      <div className="rounded-lg border border-[var(--color-border)] bg-[var(--color-bg)] p-6 shadow-lg max-w-md">
        <h2 className="text-lg font-semibold mb-2">
          This export contains AI-generated voiceover
        </h2>
        <p className="text-sm text-[var(--color-fg-muted)] mb-3">
          {`This project includes ${ttsClipCount} AI-generated TTS clip${ttsClipCount > 1 ? "s" : ""}. `}
          Under the <strong>EU AI Act</strong> Article 50, content generated by AI systems
          may require disclosure to viewers.
        </p>

        <label className="flex items-center gap-2 mb-3 text-sm cursor-pointer">
          <input
            type="checkbox"
            role="checkbox"
            checked={embedC2pa}
            onChange={(e) => setEmbedC2pa(e.target.checked)}
            className="rounded border-[var(--color-border)]"
            aria-label="Embed C2PA/XMP AI-generated marker"
          />
          <span>Embed C2PA/XMP AI-generated marker</span>
        </label>

        <a
          href="docs/compliance/ai-act.md"
          target="_blank"
          rel="noopener noreferrer"
          className="inline-flex items-center gap-1 text-xs text-[var(--color-accent)] hover:underline mb-4"
        >
          Learn more
          <ExternalLink className="h-3 w-3" />
        </a>

        <div className="flex justify-end gap-2 mt-4">
          <Button variant="ghost" onClick={handleCancel}>
            Cancel
          </Button>
          <Button onClick={handleExport}>
            Export anyway
          </Button>
        </div>
      </div>
    </div>
  );
}
