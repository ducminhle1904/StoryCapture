import { createPublicKey } from "node:crypto";
import fs from "node:fs/promises";
import path from "node:path";

function parseArgs(argv) {
  if (argv[0] === "--") argv = argv.slice(1);
  const options = {};
  for (let index = 0; index < argv.length; index += 2) {
    const key = argv[index];
    const value = argv[index + 1];
    if (!value || value.startsWith("--")) throw new Error(`Missing value for ${key}.`);
    if (key === "--signer-key-id") options.signerKeyId = value;
    else if (key === "--public-key") options.publicKey = path.resolve(value);
    else if (key === "--output") options.output = path.resolve(value);
    else throw new Error(`Unknown argument: ${key}`);
  }
  if (!options.signerKeyId || !options.publicKey || !options.output) {
    throw new Error("--signer-key-id, --public-key, and --output are required.");
  }
  return options;
}

export async function injectRecordingV3CertificationSigner(argv = process.argv.slice(2)) {
  const options = parseArgs(argv);
  const pem = await fs.readFile(options.publicKey, "utf8");
  const publicKey = createPublicKey(pem);
  if (publicKey.asymmetricKeyType !== "ed25519") {
    throw new Error("Recording V3 signer public key must be Ed25519.");
  }
  const source = [
    'import type { RecordingCertificationSignerKeyMapV3 } from "./recording-v3-certification-manifest";',
    "",
    "/** Generated by protected release automation; source checkouts remain fail-closed. */",
    "export const GENERATED_RECORDING_CERTIFICATION_SIGNER_KEYS_V3: RecordingCertificationSignerKeyMapV3 =",
    `  Object.freeze(${JSON.stringify({ [options.signerKeyId]: pem }, null, 2)});`,
    "",
  ].join("\n");
  const temporary = `${options.output}.${process.pid}.tmp`;
  await fs.writeFile(temporary, source, "utf8");
  await fs.rename(temporary, options.output);
  process.stdout.write(
    `${JSON.stringify({ signer_key_id: options.signerKeyId, output: options.output })}\n`,
  );
}

if (import.meta.url === `file://${process.argv[1]}`) {
  injectRecordingV3CertificationSigner().catch((error) => {
    process.stderr.write(`${error instanceof Error ? error.message : String(error)}\n`);
    process.exitCode = 1;
  });
}
